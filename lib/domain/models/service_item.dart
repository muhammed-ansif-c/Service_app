class ServiceItem {
  final String id;
  final String title;
  final String duration;
  final double price;
  final String rating;
  final String orders;
  final String imageUrl;

  ServiceItem({
    required this.id,
    required this.title,
    required this.duration,
    required this.price,
    required this.rating,
    required this.orders,
    required this.imageUrl,
  });
}